@dataclass(frozen=True)
class Calibration:
    """Camera intrinsics + extrinsics for the stereo pair (SI units, metres)."""

    left_intrinsics: np.ndarray  # (3, 3)
    right_intrinsics: np.ndarray  # (3, 3)
    left_to_ego: np.ndarray  # (4, 4) camera -> ego/imu
    right_to_ego: np.ndarray  # (4, 4)
    ego_to_global: np.ndarray  # (4, 4) ego/imu -> global
    left_to_global: np.ndarray  # (4, 4)
    right_to_global: np.ndarray  # (4, 4)
    stereo_baseline_m: float
    left_is_distorted: bool
    right_is_distorted: bool


@dataclass(frozen=True)
class StereoSample:
    """All per-frame outputs handed to the preprocessing module.

    See the module docstring for field semantics. Frames/datasets without a
    given modality leave the corresponding field empty (``None`` or a
    zero-length array), so consumers can stay dataset-agnostic.
    """

    # identity
    dataset: str
    log_name: str
    scene_index: int
    iteration: int
    timestamp_us: int

    # raw stereo + lidar (images are None when loaded with load_images=False)
    image_left: np.ndarray | None
    image_right: np.ndarray | None
    lidar_xyz: np.ndarray
    lidar_features: dict[
        str, np.ndarray] | None  # e.g. intensity, channel, timestamps

    # precomputed sparse depth (left image), may be None
    depth_left: np.ndarray | None

    # 3D labels (global frame). ``boxes_3d_ego`` is the same boxes in the ego
    # frame (centre + orientation transformed, extent unchanged), aligned with
    # ``lidar_xyz`` and the BEV grids — use it for BEV target encoding.
    boxes_3d: np.ndarray
    boxes_3d_ego: np.ndarray
    boxes_3d_labels: list[str]
    boxes_3d_track_tokens: list[str]
    boxes_3d_velocity: np.ndarray

    # 2D labels (left image, auto-generated from 3D)
    boxes_2d_left: np.ndarray
    boxes_2d_left_box_indices: np.ndarray

    # 3D positions of returns outside every box (ego frame) + membership mask
    points_outside_boxes_xyz: np.ndarray
    points_in_box_mask: np.ndarray

    # calibration (None when loaded with load_images=False)
    calibration: Calibration | None

    def __repr__(self) -> str:
        return (
            f"StereoSample(dataset={self.dataset!r}, log={self.log_name!r}, "
            f"iter={self.iteration}, boxes_3d={len(self.boxes_3d)}, "
            f"boxes_2d={len(self.boxes_2d_left)}, lidar_pts={len(self.lidar_xyz)}, "
            f"depth={'yes' if self.depth_left is not None else 'no'})")


@dataclass(frozen=True)
class Frame:
    """A single synchronized time step within a scene.

    Provides dataset-agnostic lazy accessors for individual modalities
    (:meth:`camera`, :meth:`lidar`, :meth:`boxes`, :meth:`ego_state`,
    :meth:`depth`) and the project-level :meth:`to_stereo_sample`, which bundles
    them into a :class:`StereoSample`.
    """

    scene: SceneAPI
    scene_index: int
    iteration: int
    depth_root: Path | None = None

    # -- identity / metadata -------------------------------------------------
    @property
    def dataset(self) -> str:
        return self.scene.dataset

    @property
    def split(self) -> str:
        return self.scene.split

    @property
    def location(self) -> str:
        return self.scene.location

    @property
    def log_name(self) -> str:
        return self.scene.log_name

    @property
    def scene_uuid(self) -> str:
        return self.scene.scene_uuid

    @property
    def timestamp(self) -> Timestamp:
        return self.scene.get_timestamp_at_iteration(self.iteration)

    @property
    def available_camera_ids(self) -> list[CameraID]:
        return self.scene.available_camera_ids

    @property
    def available_lidar_ids(self) -> list[LidarID]:
        return self.scene.available_lidar_ids

    # -- modality accessors --------------------------------------------------
    def camera(self, camera_id: CameraIdLike) -> Camera | None:
        """Return the :class:`Camera` (image + intrinsics/extrinsics) for ``camera_id``."""
        return self.scene.get_camera_at_iteration(self.iteration, camera_id)

    def cameras(
        self,
        camera_ids: Sequence[CameraIdLike] | None = None
    ) -> dict[CameraID, Camera]:
        """Return ``{CameraID: Camera}`` for the requested (or all available) cameras."""
        ids = list(camera_ids
                   ) if camera_ids is not None else self.available_camera_ids
        out: dict[CameraID, Camera] = {}
        for cid in ids:
            camera = self.camera(cid)
            if camera is not None:
                out[CameraID.from_arbitrary(cid)] = camera
        return out

    def lidar(self, lidar_id: LidarIdLike | None = None) -> Lidar | None:
        """Return a :class:`Lidar` point cloud.

        Defaults to the merged point cloud when present, otherwise the first
        available lidar, so callers need not know each dataset's lidar layout.
        """
        if lidar_id is None:
            lidar_id = self._default_lidar_id()
            if lidar_id is None:
                return None
        return self.scene.get_lidar_at_iteration(self.iteration, lidar_id)

    def boxes(self) -> BoxDetectionsSE3 | None:
        """Return the 3D bounding-box detections (ground-truth labels), if any."""
        return self.scene.get_box_detections_se3_at_iteration(self.iteration)

    def ego_state(self) -> EgoStateSE3 | None:
        """Return the ego-vehicle state (pose, dynamics), if available."""
        return self.scene.get_ego_state_se3_at_iteration(self.iteration)

    def depth(self) -> np.ndarray | None:
        """Load the precomputed sparse depth map (left image), or ``None``.

        Looks for ``<depth_root>/<log_name>/<iteration:05d>.npz`` with key
        ``depth_map``. The map is LiDAR projected into the left stereo camera:
        a ``(H, W)`` float32 array with 0 where there is no return.
        """
        if self.depth_root is None:
            return None
        path = Path(
            self.depth_root) / self.log_name / f"{self.iteration:05d}.npz"
        if not path.exists():
            return None
        with np.load(path) as data:
            key = "depth_map" if "depth_map" in data.files else data.files[0]
            return data[key]

    def to_stereo_sample(
        self,
        left_camera_id: CameraIdLike = DEFAULT_LEFT_CAMERA,
        right_camera_id: CameraIdLike = DEFAULT_RIGHT_CAMERA,
        lidar_id: LidarIdLike | None = None,
        load_images: bool = True,
        point_mask: bool = True,
    ) -> StereoSample:
        """Assemble the per-frame :class:`StereoSample` for preprocessing.

        The two flags exist because full assembly costs ~0.9 s/frame while the
        training loop needs almost none of it (profiled: the in-box point mask
        dominates, then image decode + 2D projection):

        * ``load_images=False`` — skip the stereo pair entirely: ``image_*``,
          ``depth_left`` and ``calibration`` are ``None``, ``boxes_2d_*`` empty.
          Enough for LiDAR training and for the camera branch fed from the
          precomputed stereo cache (which stores its own K / extrinsics).
        * ``point_mask=False`` — skip the LiDAR-in-box test (the single most
          expensive step): ``points_in_box_mask`` is all-False and
          ``points_outside_boxes_xyz`` empty, i.e. *not computed*.

        :param left_camera_id: Left stereo camera id (default ``pcam_stereo_l``;
            KITTI-360 ``image_00`` / AV2 stereo-left both map here).
        :param right_camera_id: Right stereo camera id (default ``pcam_stereo_r``;
            KITTI-360 ``image_01`` / AV2 stereo-right).
        :param lidar_id: Lidar to use; defaults to merged/first available.
        :raises ValueError: if the requested stereo cameras are not available.
        """
        left = right = None
        if load_images:
            left = self.camera(left_camera_id)
            right = self.camera(right_camera_id)
            if left is None or right is None:
                available = [c.name for c in self.available_camera_ids]
                raise ValueError(
                    f"Stereo cameras {left_camera_id!r}/{right_camera_id!r} not available; "
                    f"this scene exposes {available}. Pass left_camera_id/right_camera_id "
                    f"for your dataset's stereo pair.")

        lidar = self.lidar(lidar_id)
        lidar_xyz = lidar.xyz.astype(
            np.float64) if lidar is not None else np.zeros(
                (0, 3), dtype=np.float64)
        lidar_features = lidar.point_cloud_features if lidar is not None else None

        boxes = self.boxes()
        boxes_array = _boxes_to_array(boxes)
        detections = boxes.box_detections if boxes is not None else []
        boxes_labels = [
            str(
                getattr(d.attributes.default_label, "name",
                        d.attributes.default_label)) for d in detections
        ]
        boxes_tokens = [d.attributes.track_token for d in detections]
        boxes_velocity = (np.stack(
            [np.asarray(d.velocity_3d, dtype=np.float64)
             for d in detections]) if detections else np.zeros(
                 (0, 3), dtype=np.float64))

        # 2D boxes: project the 3D (global) boxes into the left image. The 2D
        # projection consumes global boxes, so ``boxes_array`` stays global.
        if left is not None:
            boxes_2d, boxes_2d_indices = _project_boxes_2d(left, boxes_array)
        else:
            boxes_2d = np.zeros((0, 4), dtype=np.float64)
            boxes_2d_indices = np.zeros((0, ), dtype=np.int64)

        # 3D positions of returns outside every box (background geometry).
        ego = self.ego_state()

        # Ego-frame copy of the boxes for BEV target encoding (aligned with the
        # lidar / BEV grids). Centre + orientation are transformed; extent is
        # invariant. Without an ego pose we cannot convert, so leave it empty.
        if boxes_array.shape[0] and ego is not None:
            boxes_ego = boxes_global_to_ego(boxes_array, ego.imu_se3)
            assert_boxes_in_sensor_range(boxes_ego)
        else:
            boxes_ego = np.zeros((0, 10), dtype=np.float64)
        if point_mask and lidar_xyz.shape[0] and boxes_array.shape[0] and ego is not None:
            points_global = rel_to_abs_points_3d_array(ego.imu_se3, lidar_xyz)
            inside = points_3d_in_bbse3_array(points_global.astype(np.float64),
                                              boxes_array)  # (N_boxes, P)
            in_any_box = inside.any(axis=0)
            points_outside = lidar_xyz[~in_any_box]
        else:
            in_any_box = np.zeros((lidar_xyz.shape[0], ), dtype=bool)
            # all points when the mask is trivially empty, none when skipped
            points_outside = lidar_xyz if point_mask else np.zeros(
                (0, 3), dtype=np.float64)

        # Calibration (needs the camera metadata, so only with images loaded).
        if left is not None:
            ego_to_global = _pose_to_matrix(
                ego.imu_se3) if ego is not None else np.eye(4)
            left_to_ego = _pose_to_matrix(left.metadata.camera_to_imu_se3)
            right_to_ego = _pose_to_matrix(right.metadata.camera_to_imu_se3)
            baseline = float(
                np.linalg.norm(left_to_ego[:3, 3] - right_to_ego[:3, 3]))
            calibration = Calibration(
                left_intrinsics=_intrinsics_matrix(left.metadata.intrinsics),
                right_intrinsics=_intrinsics_matrix(right.metadata.intrinsics),
                left_to_ego=left_to_ego,
                right_to_ego=right_to_ego,
                ego_to_global=ego_to_global,
                left_to_global=_pose_to_matrix(left.camera_to_global_se3),
                right_to_global=_pose_to_matrix(right.camera_to_global_se3),
                stereo_baseline_m=baseline,
                left_is_distorted=bool(left.metadata.is_distorted),
                right_is_distorted=bool(right.metadata.is_distorted),
            )
        else:
            calibration = None

        return StereoSample(
            dataset=self.dataset,
            log_name=self.log_name,
            scene_index=self.scene_index,
            iteration=self.iteration,
            timestamp_us=self.timestamp.time_us,
            image_left=left.image if left is not None else None,
            image_right=right.image if right is not None else None,
            lidar_xyz=lidar_xyz,
            lidar_features=lidar_features,
            depth_left=self.depth() if load_images else None,
            boxes_3d=boxes_array,
            boxes_3d_ego=boxes_ego,
            boxes_3d_labels=boxes_labels,
            boxes_3d_track_tokens=boxes_tokens,
            boxes_3d_velocity=boxes_velocity,
            boxes_2d_left=boxes_2d,
            boxes_2d_left_box_indices=boxes_2d_indices,
            points_outside_boxes_xyz=points_outside,
            points_in_box_mask=in_any_box,
            calibration=calibration,
        )

    def _default_lidar_id(self) -> LidarID | None:
        available = self.available_lidar_ids
        if not available:
            return None
        if LidarID.LIDAR_MERGED in available:
            return LidarID.LIDAR_MERGED
        return available[0]

    def __repr__(self) -> str:
        return (f"Frame(dataset={self.dataset!r}, log={self.log_name!r}, "
                f"scene={self.scene_index}, iter={self.iteration})")


class Py123dDataset:
    """A flat, frame-indexed view over a filtered set of py123d scenes.

    Scenes are selected with a :class:`~py123d.api.SceneFilter`; every iteration
    (synchronized time step) of every matching scene becomes one indexable
    :class:`Frame`. The map-style ``__len__`` / ``__getitem__`` interface plugs
    directly into a ``torch.utils.data.DataLoader`` (call ``to_stereo_sample``
    in your ``collate``/transform) without importing torch here.

    :param data_root: Root holding converted ``logs/`` (and ``maps/``,
        ``preprocessed/``). Defaults to ``PY123D_DATA_ROOT`` or the repo
        ``dataset/`` dir.
    :param split_names: Split filter, e.g. ``["kitti360_train"]``.
    :param datasets: Dataset-name filter, e.g. ``["kitti360"]``.
    :param depth_root: Directory of precomputed depth maps. Defaults to
        ``<data_root>/preprocessed/depth_maps``.
    :param scene_filter: A fully-built :class:`SceneFilter`; if given, the
        individual filter kwargs above are ignored.
    :param filter_kwargs: Any other :class:`SceneFilter` argument
        (``log_names``, ``scene_uuids``, ``max_num_scenes``, ``shuffle``, ...).
    """

    def __init__(
        self,
        data_root: str | Path | None = None,
        *,
        split_names: Sequence[str] | None = None,
        datasets: Sequence[str] | None = None,
        depth_root: str | Path | None = None,
        scene_filter: SceneFilter | None = None,
        **filter_kwargs,
    ) -> None:
        self.data_root = configure_dataset_paths(data_root)
        self.depth_root = Path(
            depth_root
        ) if depth_root is not None else self.data_root / "preprocessed" / "depth_maps"

        if scene_filter is None:
            scene_filter = SceneFilter(
                datasets=list(datasets) if datasets is not None else None,
                split_names=list(split_names)
                if split_names is not None else None,
                **filter_kwargs,
            )
        self.scene_filter = scene_filter

        self.scenes: List[SceneAPI] = get_filtered_scenes(scene_filter, data_root=self.data_root)
        print(f"DEBUG: found {len(self.scenes)} scenes using filter {scene_filter}", flush=True)
        # Flat index: one entry per (scene, iteration) so frames are addressable
        # by a single integer. ``number_of_iterations`` counts current + future.
        self._index: list[tuple[int, int]] = [
            (scene_idx, iteration)
            for scene_idx, scene in enumerate(self.scenes)
            for iteration in range(scene.number_of_iterations)
        ]

    @property
    def scene_count(self) -> int:
        return len(self.scenes)

    def __len__(self) -> int:
        return len(self._index)

    def _make_frame(self, scene_index: int, iteration: int) -> Frame:
        return Frame(
            scene=self.scenes[scene_index],
            scene_index=scene_index,
            iteration=iteration,
            depth_root=self.depth_root,
        )

    def __getitem__(self, index: int) -> Frame:
        scene_index, iteration = self._index[index]
        return self._make_frame(scene_index, iteration)

    def __iter__(self) -> Iterator[Frame]:
        for index in range(len(self)):
            yield self[index]

    def get_frame(self, scene_index: int, iteration: int) -> Frame:
        """Return the :class:`Frame` for an explicit ``(scene_index, iteration)``."""
        return self._make_frame(scene_index, iteration)

    def frames_in_scene(self, scene_index: int) -> Iterator[Frame]:
        """Iterate the frames of a single scene in capture order."""
        for iteration in range(self.scenes[scene_index].number_of_iterations):
            yield self._make_frame(scene_index, iteration)

    def __repr__(self) -> str:
        return (f"Py123dDataset(data_root={str(self.data_root)!r}, "
                f"scenes={self.scene_count}, frames={len(self)})")


