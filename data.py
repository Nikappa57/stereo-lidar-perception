def configure_dataset_paths(data_root: str | Path | None = None) -> Path:
    """Point py123d at the dataset roots and return the resolved data root.

    The data root is taken from (in order): the ``data_root`` argument, the
    ``PY123D_DATA_ROOT`` environment variable, or :data:`DEFAULT_DATA_ROOT`.

    Any already-exported dataset root variable (``AV2_DATA_ROOT`` etc.) is left
    untouched; the rest default to ``data_root`` so the self-contained repo
    layout works out of the box while real installs keep their own paths.

    Must be called before any scene/sensor data is read, because the readers
    look up sensor locations through the process-global dataset paths. It is
    invoked automatically by :class:`Py123dDataset`.
    """
    env_root = os.environ.get("PY123D_DATA_ROOT")
    if env_root == "None":
        env_root = None
    resolved = Path(data_root or env_root or DEFAULT_DATA_ROOT).resolve()
    if not (resolved / "logs").exists():
        print(f"DEBUG: logs not found in {resolved}, falling back to {DEFAULT_DATA_ROOT}", flush=True)
        resolved = Path(DEFAULT_DATA_ROOT).resolve()
    print(f"DEBUG: PY123D_DATA_ROOT resolved to {resolved}", flush=True)

    os.environ["PY123D_DATA_ROOT"] = str(resolved)
    # The raw KITTI-360 sensor blobs (calibration/ data_2d_raw/ data_3d_raw) live
    # in a ``KITTI-360`` dir, NOT under the logs root — point KITTI360_DATA_ROOT
    # there when present, before the generic fill-in below defaults it to
    # ``resolved``. Check the data root first (``<root>/KITTI-360``, e.g.
    # ``dataset/KITTI-360``), then the repo top level (``<repo>/KITTI-360``).
    for kitti360_root in (resolved / "KITTI-360", _REPO_ROOT / "KITTI-360"):
        if kitti360_root.is_dir():
            os.environ.setdefault("KITTI360_DATA_ROOT", str(kitti360_root))
            break
    for env_var in _DATASET_ROOT_ENV_VARS:
        # ``setdefault`` keeps user-provided roots; only fills in the gaps.
        os.environ.setdefault(env_var, str(resolved))

    # Initialise the process-global paths from the (now-populated) environment.
    setup_dataset_paths(DatasetPaths.from_env())
    return resolved


