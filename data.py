"""Dataset loading for the stereo-lidar-perception project.

This module is the *data* stage of the pipeline. It loads raw sensor data,
calibration and labels through the unified ``py123d`` Scene API and assembles
them into a per-frame :class:`StereoSample`, then provides the geometric
*preprocessing* representations (stereo depth/BEV, frustum, voxel, clustering)
that the network and baselines consume.

Because everything goes through :class:`py123d.api.SceneAPI`, the same code
works for any py123d dataset (KITTI-360, Argoverse 2, nuPlan, nuScenes, ...).
Switching datasets only changes the :class:`~py123d.api.SceneFilter` and the
root paths; the stereo-specific assembly assumes a left/right camera pair whose
ids are configurable (``pcam_stereo_l`` / ``pcam_stereo_r`` by default — see
:mod:`globals`). The project default is KITTI-360 (the only py123d dataset with
a real colour stereo pair; see ``docs/dataset.md``).

============================ INPUTS ============================
Read from disk (paths resolved from environment variables, see
:func:`configure_dataset_paths` and ``py123d-docs/installation.md``):

* Converted py123d logs (Arrow) under ``$PY123D_DATA_ROOT/logs`` — ego state,
  camera/lidar tables, 3D box labels, sync table.
* Original sensor blobs under the per-dataset root (e.g. KITTI-360's
  ``$KITTI360_DATA_ROOT/data_2d_raw`` + ``data_3d_raw``, AV2's
  ``$AV2_DATA_ROOT/sensor``) — the PNG/JPEG images and lidar scans (conversion
  stores only relative paths to these).
* Precomputed sparse depth maps under
  ``$PY123D_DATA_ROOT/preprocessed/depth_maps/<log>/<iteration>.npz`` (key
  ``depth_map``): LiDAR projected into the left stereo image. Optional — frames
  load ``None`` when a depth file is absent.
* A :class:`~py123d.api.SceneFilter` selecting dataset / split / scenes.

============================ OUTPUTS ===========================
One :class:`StereoSample` per frame (consumed by the representations below):

* ``image_left`` / ``image_right`` — stereo RGB pair, ``(H, W, 3)`` uint8.
* ``depth_left`` — precomputed sparse metric depth in the left image,
  ``(H, W)`` float32 (0 = no LiDAR return), or ``None`` if not available.
* ``lidar_xyz`` — full point cloud ``(P, 3)`` in the **ego** frame;
  ``lidar_features`` — dict of per-point channels (intensity, channel,
  timestamps, ...).
* ``boxes_3d`` — ground-truth 3D boxes, ``(N, 10)`` in the **global** frame
  (``BoundingBoxSE3Index``: x,y,z,qw,qx,qy,qz,length,width,height), plus
  ``boxes_3d_labels`` / ``boxes_3d_track_tokens`` / ``boxes_3d_velocity``.
* ``boxes_3d_ego`` — the same boxes in the **ego** frame (centre + orientation
  transformed, extent unchanged), aligned with ``lidar_xyz`` and the BEV grids.
  Use this — not ``boxes_3d`` — for BEV target encoding; ``boxes_3d`` stays
  global because the 2D projection below needs it there.
* ``boxes_2d_left`` — image-plane 2D boxes auto-generated by projecting the 3D
  boxes into the left camera, ``(M, 4)`` xyxy (clipped to the image), with
  ``boxes_2d_left_box_indices`` mapping each back to its row in ``boxes_3d``.
* ``points_outside_boxes_xyz`` — 3D positions (ego frame) of LiDAR returns that
  fall outside *every* 3D box (static/background geometry), plus the
  ``points_in_box_mask`` over ``lidar_xyz``.
* ``calibration`` — :class:`Calibration` with the left/right intrinsics, the
  camera->ego and ego->global extrinsics, and the stereo baseline.

Quick start::

    from data import Py123dDataset
    dataset = Py123dDataset(split_names=["kitti360_train"])
    sample = dataset[0].to_stereo_sample()   # -> StereoSample for preprocessing
"""

