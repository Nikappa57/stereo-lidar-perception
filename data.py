def _pose_to_matrix(pose) -> np.ndarray:
    """Convert a py123d ``PoseSE3`` to a 4x4 homogeneous transform."""
    matrix = np.eye(4, dtype=np.float64)
    matrix[:3, :3] = np.asarray(pose.rotation_matrix, dtype=np.float64)
    matrix[:3, 3] = np.asarray(pose.array, dtype=np.float64)[:3]
    return matrix


def _intrinsics_matrix(intrinsics) -> np.ndarray:
    """Build a 3x3 camera matrix from py123d ``PinholeIntrinsics`` (fx,fy,cx,cy,skew)."""
    values = np.asarray(intrinsics, dtype=np.float64).ravel()
    fx, fy, cx, cy = values[0], values[1], values[2], values[3]
    skew = values[4] if values.size > 4 else 0.0
    return np.array([[fx, skew, cx], [0.0, fy, cy], [0.0, 0.0, 1.0]],
                    dtype=np.float64)


def _boxes_to_array(boxes: BoxDetectionsSE3 | None) -> np.ndarray:
    """Stack box poses+dims into ``(N, 10)`` (``BoundingBoxSE3Index``), global frame."""
    if boxes is None or not boxes.box_detections:
        return np.zeros((0, 10), dtype=np.float64)
    return np.stack([d.bounding_box_se3.array
                     for d in boxes.box_detections]).astype(np.float64)


# Generous bound on how far a real label can sit from the ego vehicle, in
# metres. AV2/nuScenes/Waymo annotate well inside this; a *global*-frame box fed
# in by mistake lands at city-scale coordinates (often >1 km from the origin)
# and trips the guard. This is a frame-mismatch tripwire, *not* a grid crop.
SENSOR_RANGE_M = 500.0


def boxes_global_to_ego(boxes_global: np.ndarray, ego_origin) -> np.ndarray:
    """Convert ``(N, 10)`` 3D boxes from the global frame to the ego/imu frame.

    The labels in :attr:`StereoSample.boxes_3d` live in the **global** frame,
    whereas the lidar point cloud, every BEV grid and the splatted features live
    in the **ego** frame. A target encoder that rasterises box centres onto the
    BEV grid must therefore convert first, or the targets land in the wrong cells
    (a silent, non-crashing bug that simply fails to converge).

    Both the centre ``(x, y, z)`` *and* the orientation quaternion
    ``(qw, qx, qy, qz)`` are transformed (via :func:`abs_to_rel_se3_array`, the
    documented inverse of the ``rel_to_abs`` used to lift the lidar to global);
    the extent ``(length, width, height)`` is frame-invariant and copied through.

    :param boxes_global: ``(N, 10)`` array indexed by
        :class:`~py123d.geometry.BoundingBoxSE3Index`, in the global frame.
    :param ego_origin: The ego pose in the global frame — a py123d ``PoseSE3``
        (e.g. ``ego_state.imu_se3``) or its ``(7,)`` ``PoseSE3Index`` array.
    :return: ``(N, 10)`` array of the same boxes in the ego frame.
    """
    boxes_global = np.asarray(boxes_global, dtype=np.float64)
    if boxes_global.shape[0] == 0:
        return boxes_global.reshape(0, len(BoundingBoxSE3Index)).copy()

    boxes_ego = boxes_global.copy()
    # SE3 == (x, y, z, qw, qx, qy, qz); same layout as PoseSE3Index, so the pose
    # transform applies directly. EXTENT (l, w, h) is left untouched.
    boxes_ego[:, BoundingBoxSE3Index.SE3] = abs_to_rel_se3_array(
        ego_origin, boxes_global[:, BoundingBoxSE3Index.SE3])
    return boxes_ego


def assert_boxes_in_sensor_range(boxes_ego: np.ndarray,
                                 max_radius_m: float = SENSOR_RANGE_M) -> None:
    """Frame-mismatch guard: assert ego-frame box centres are sensor-scale.

    Cheap tripwire against silently feeding *global*-frame boxes where ego-frame
    boxes are expected (e.g. after a dataset swap): global centres are city-scale
    and blow past ``max_radius_m``, ego centres never do. It deliberately does
    **not** assert boxes fall inside the BEV crop — legitimate labels sit behind
    the ego or beyond the grid; cropping to ``x_range``/``y_range`` is the target
    encoder's job, not a correctness invariant of the frame conversion.

    :raises AssertionError: if any centre is non-finite or farther than
        ``max_radius_m`` from the ego origin in the x/y plane.
    """
    if boxes_ego.shape[0] == 0:
        return
    centres = boxes_ego[:, BoundingBoxSE3Index.XYZ]
    assert np.isfinite(centres).all(), "box centres contain non-finite values"
    radius = np.linalg.norm(centres[:, :2], axis=1)
    worst = float(radius.max())
    assert worst <= max_radius_m, (
        f"box centre {worst:.1f} m from ego exceeds {max_radius_m:.0f} m sensor range — "
        "boxes look like they are still in the global frame (forgot global->ego conversion?)"
    )


def _project_boxes_2d(camera: Camera, boxes_array: np.ndarray):
    """Project 3D boxes into an image and return clipped amodal 2D boxes.

    :return: ``(boxes_2d, box_indices)`` where ``boxes_2d`` is ``(M, 4)`` xyxy in
        pixels (clipped to the image) and ``box_indices`` maps each 2D box to its
        row in ``boxes_array``. Boxes with no corner in front of the camera are
        dropped.
    """
    if boxes_array.shape[0] == 0:
        return np.zeros((0, 4), dtype=np.float64), np.zeros((0, ),
                                                            dtype=np.int64)

    corners_global = bbse3_array_to_corners_array(boxes_array).reshape(
        -1, 3)  # (N*8, 3)
    pixels, in_fov, depth = camera.project_points_global(corners_global)
    pixels = pixels.reshape(-1, 8, 2)
    in_fov = in_fov.reshape(-1, 8)
    in_front = depth.reshape(-1, 8) > 0.0

    width, height = camera.metadata.width, camera.metadata.height
    boxes_2d: list[np.ndarray] = []
    indices: list[int] = []
    for i in range(boxes_array.shape[0]):
        front = in_front[i]
        if not front.any() or not in_fov[i].any():
            continue
        # Amodal extent from the corners that are in front of the camera.
        corner_px = pixels[i][front]
        x_min, y_min = corner_px.min(axis=0)
        x_max, y_max = corner_px.max(axis=0)
        boxes_2d.append(
            np.array(
                [
                    np.clip(x_min, 0, width),
                    np.clip(y_min, 0, height),
                    np.clip(x_max, 0, width),
                    np.clip(y_max, 0, height),
                ],
                dtype=np.float64,
            ))
        indices.append(i)

    if not boxes_2d:
        return np.zeros((0, 4), dtype=np.float64), np.zeros((0, ),
                                                            dtype=np.int64)
    return np.stack(boxes_2d), np.asarray(indices, dtype=np.int64)


